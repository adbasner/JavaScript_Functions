function reverseString(str) {
  return str.split("").reverse().join("");
}

console.log("Hello backwards is: " + reverseString("hello")); // olleh
console.log("Howdy backwards is: " + reverseString("Howdy")); // ydwoH
console.log("Greetings from Earth backwards is: " + reverseString("Greetings from Earth")); // htrae morf sgniteerG
